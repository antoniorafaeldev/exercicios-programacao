/* Tipifique uma função JavaScript que recebe o nome de um usuário e sua idade para retornar uma mensagem de boas-vindas. */

function showWelcomeMessage(name: string, age: number): void {
    console.log(`Bem vindo ${name}, você tem ${age} anos.`)
}

showWelcomeMessage("Ronald", 21)
showWelcomeMessage("Alisson", 22)
showWelcomeMessage("Rayan", 34)